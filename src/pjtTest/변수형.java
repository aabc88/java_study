package pjtTest;

public class 변수형 {
    public static void main(String[] args) {
        char c = 'a';
        System.out.println("c = "+c);

        int i = 10;
        System.out.println("i = "+i);

        double d = 3.1234;
        System.out.println("d = "+d);

        boolean b = false;
        System.out.println("b = "+b);

        String str = "hello world";
        System.out.println("str = "+str);
    }
}
